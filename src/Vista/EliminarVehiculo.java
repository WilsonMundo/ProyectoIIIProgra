    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {

            String persona = "";
            String modelo = "";
            String linea = "";
            String color = "";

            if (!jtPersona.getText().trim().isEmpty() || !(jtPersona.getText() == null)) {
                persona = jtPersona.getText();
            }
            if (!jfModelo.getText().trim().isEmpty() || !(jfModelo.getText() == null)) {
                modelo = jfModelo.getText();
            }
            if (!jfLinea.getText().trim().isEmpty() || !(jfLinea.getText() == null)) {
                linea = jfLinea.getText();
            }
            if (!jfColor.getText().trim().isEmpty() || !(jfColor.getText() == null)) {
                color = jfColor.getText();
            }

            ArrayList<NodoVehiculo> nodoVehiculo = Servicioparqueo.parqueoModelo.buscarMultiplesCampos(linea, color, persona, modelo);
            if (nodoVehiculo != null && nodoVehiculo.size() > 0) {
                llenarTabla(nodoVehiculo);
                limpiarCampos();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar el vehiculo: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton3ActionPerformed


