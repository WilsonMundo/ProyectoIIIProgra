    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try {
            if (jtPlaca.getText().trim().isEmpty() || jtPlaca.getText() == null) {
                JOptionPane.showMessageDialog(this, "Error la placa no puede ser vacio", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Vehiculo vehiculo = new Vehiculo();
            NodoVehiculo nVehiculoRegistrado = Servicioparqueo.parqueoModelo.buscarPorPlaca(jtPlaca.getText());
            if (nVehiculoRegistrado != null) {
                newVehiculo = nVehiculoRegistrado;
                ArrayList<NodoVehiculo> nodoVehiculo = new ArrayList<NodoVehiculo>();
                nodoVehiculo.add(newVehiculo);
                llenarTabla(nodoVehiculo);
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "la placa no existe", "invalido", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar el vehiculo: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed
    private void llenarTabla(ArrayList<NodoVehiculo> nodoVehiculos) {
        try {
            /* Crear modelo de tabla y establecerlo en el JTable*/
            DefaultTableModel modelos = (DefaultTableModel) jTable1.getModel();
            while (modelos.getRowCount() > 0) {
                modelos.removeRow(0);
            }
            DefaultTableModel modelo = new DefaultTableModel(new Object[]{"Placa", "Linea", "Propietario", "Color", "Modelo"
            }, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    //------ Devuelve false para que ninguna celda sea editable.
                    return false;
                }
            };

            jTable1.setModel(modelo);

            for (NodoVehiculo tipo : nodoVehiculos) {
                modelo.addRow(new Object[]{tipo.getVehiculo().getPlaca(),
                    tipo.getVehiculo().getLinea(), tipo.getVehiculo().getPropietario(),
                    tipo.getVehiculo().getColor(),
                    tipo.getVehiculo().getModelo()});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar la persona: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
